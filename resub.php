<h1>About Resubmitting Assignments</h1>

<p>There are two 

There are two situations in which it may make sense to resubmit
an assignment. Please read the information below carefully.</p>

<hr />

<p><b><u>Replacing a previous submission that is not yet graded</u></b></p>

  <p>If you have submitted an assignment that has not yet been graded
  and you want to submit a newer (presumably better) 
   version, simply <b>use the normal "Upload" button</b> on the assignment
   page. The new submission will officially replace the old one; however,
<b>its timestamp will be updated</b> and the standard late policy 
will apply based on the new timestamp.</p>

<hr />

<p><b><u>Requesting a regrade</u></b></p>

<p>All assignments are graded with the assistance of an automated script
  which attempts to compile the code you submit and, in some cases, combine it with
  testing code. In some cases, this means a <b>small error</b> in your submitted code
  ends up leading to <b>large point deductions</b>. Most often, this happens because
  the code you submitted could not be compiled, and is automatically assigned a score of 0.</p>

<p>To request a regrade, do all of the following:
  <ul>
    <li>Read the comments attached to your score.
      <li>Look at your code and figure out what caused the problem(s)
described in the comments. Discuss with me in person or over email if you need
help.
<li>IF the problem you identified is small
compared to the number of points you lost -- e.g., filling in
a missing semicolon, fixing a typo or making another relatively
trivial adjustment -- you may request a regrade. 
<li>Request a regrade
by contacting me <b>directly</b> by email:
<b>lpc.cschatz@gmail.com</b>.<br />
In the email:
<ul>
<li>State which assignment you are requesting a regrade for.
<li>Explain what changes you made.
<li>Attach a copy of the updated submission.
</ul>
<li>Do <b>NOT</b> use the normal "Upload" button on SciNet. 
</ul>
</p>

<p>Other information about regrades:
<ul>
<li>A regraded assignment will be scored as if it was submitted
in its edited form the first time, <b>minus</b> a penalty for whatever
changes had to be made - around 10% but depending on the nature of the
changes.
<li>In some cases, a "could not be compiled" score of 0 may
be due to a mistake made by me or my grading script. No penalty
applies if this is the case.
<li>Regrades are ONLY for minor problems that led to point 
deductions BEYOND the problems themselves. Your submissions are
NOT eligible for a regrade for:
<ul>
<li>adding functionality that was missing in the original submission
<li>fixing substantive problems after an assignment deadline and getting full credit
as if you had submitted on time
<li>changes to your original code that require affect more than a small fraction of the number of lines in program
</p>

      
