<p><b>Interactive Fiction</b><br />
     To play interactive fiction games, you first need an <b>interpreter</b>.
The recommended interpreter is called <i>Gargoyle</i>. Pick the appropriate download below:
<ul>
<li><a href="http://code.google.com/p/garglk/downloads/detail?name=gargoyle-2011.1-mac.dmg" target="_blank">Gargoyle for Mac</a>
<li><a href="http://code.google.com/p/garglk/downloads/detail?name=gargoyle-2011.1-windows.exe" target="_blank">Gargoyle for Windows</a>
</ul>
   Next, you need a game to play. The best way to find a game to play,
   in general, is to browse <a href="http://ifdb.tads.org/" target="_blank">IFDB</a> ("Interactive Fiction Database"). Games I particularly recommend starting
   with include:
<ul>
<li><a href="http://ifdb.tads.org/viewgame?id=qzftg3j8nh5f34i2" target="_blank">9:05</a>
<li><a href="http://ifdb.tads.org/viewgame?id=sj4rxxt9ec0n5zy9" target="_blank">Gourmet</a>
<li><a href="http://ifdb.tads.org/viewgame?id=fn8r65rg7upfff0o" target="_blank">Englightenment</a>
<li><a href="http://ifdb.tads.org/viewgame?id=mohwfk47yjzii14w" target="_blank">Lost Pig</a>
<li><a href="http://ifdb.tads.org/viewgame?id=4glrrfh7wrp9zz7b" target="_blank">Violet</a>
</ul>
</p>

   <p>For a good introduction to standard commands used in games like this,
take a look at<br /><a href="http://inform7.com/if/anth/IntroductionToIF.pdf">
Emily Short's manual on playing Interactive Fiction</a>,<br />
or Andrew Plotkin's <a href="http://pr-if.org/doc/play-if-card/play-if-card.pdf">One-page reference card for common commands.</a></p>

