<p><b>Code::Blocks</b><br />
Code::Blocks is an integrated development environment (IDE) for C++ programming. It is 
open-source and free to download and install. There are versions for Windows, Mac and Linux.
<ul>
<li><a href="http://sourceforge.net/projects/codeblocks/files/Binaries/13.12/Windows/codeblocks-13.12mingw-setup.exe">Code::Blocks (Windows)</a>
</ul>
     <p><b>Xcode</b> (Mac users only)<br />
     Even though there is a Mac version of Code::Blocks, I recommend that
Mac users work with Apple's standard development environment:
<ul>
<li><a href="https://developer.apple.com/xcode/" target="_blank">Xcode</a>
</ul>
</p>
