<b>Media from Scratch</b><br />
This is a .zipped folder containing image files from MIT's "Scratch" 
platform. They can be imported into Stencyl projects for actor
images or backgrounds. (There are also many sound files, but note
that any .wav format filed need to be converted to .mp3 using a tool
like <a href="http://audacity.sourceforge.net/download/" target="_blank">
Audacity</a>.
